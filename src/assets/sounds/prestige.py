# Prestige sound module for the idle game
"""
Prestige sound module for the idle game
"""
def get_sound(sound_name: str) -> str:
    """
    Get sound file path for the given sound name
    """
    if sound_name == "prestige":
        return "sounds/sound_effects/prestige.mp3"
    return "sounds/sound_effects/prestige.mp3"
