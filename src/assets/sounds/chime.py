# Chime sound module for the idle game
"""
Chime sound module for the idle game
"""
def get_sound(sound_name: str) -> str:
    """
    Get sound file path for the given sound name
    """
    if sound_name == "chime":
        return "sounds/sound_effects/chime.mp3"
    return "sounds/sound_effects/chime.mp3"
